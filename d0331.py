import matplotlib.pyplot as plt

for ID_idx, ID in enumerate(IDs):
    if ID_idx % 23 != 0:
        continue
    a_list = []
    b_list = []
    for root_idx, root in enumerate(roots):
        a_list.append(np.load(os.path.join(root, "Hsegment_10_whole", "ecg", "ecg_"+ID+'.npy')))
        b_list.append(np.load(os.path.join(root, "segment_10_whole", "ecg", "ecg_"+ID+'.npy')))
    for i in range(200, a_list[0].shape[0], 97):
        fig, axes = plt.subplots(11, 2, figsize=(20, 10))
        len_list = []
        for root_idx, root in enumerate(roots):
            a = a_list[root_idx][i, :, :]
            b = b_list[root_idx][i, :, :]
            len_list.append(a.shape[0])
            assert a.shape[0] == b.shape[0], f"a.shape[0]={a.shape[0]} != b.shape[0]={b.shape[0]}"
            for j in range(11):
                if j < 3:
                    axes[j, root_idx].plot(a[:, j])
                else:
                    axes[j, root_idx].plot(b[:, j-3])
                axes[j, root_idx].grid(True)
        plt.savefig(f"ecg_{ID}_{i}.png")
        plt.close()

        assert len_list.count(len_list[0]) == len(len_list), f"len_list={len_list} is not equal"
    if ID_idx >=80:
        break